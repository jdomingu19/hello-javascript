// 1. var
var myString = "Hello, JavaScript!";
console.log(myString); // Hello, JavaScript!

myString = "This is a String";
console.log(myString); // This is a String

// 2. let
let myString2 = "Hello, Node.js!";
console.log(myString2); // Hello, Node.js!

myString2 = "This is also a String";
console.log(myString2); // This is also a String

// 3. const
const myString3 = "Hello, React!";
console.log(myString3); // Hello, React!

